    public virtual void SetTiles(Tile[] blockTiles)
    {

        tiles = blockTiles;
    }

    public virtual Tile TexturePosition(Direction direction)
    {
        Tile tile = new Tile();

        //tile.x = tiles[0].x;
        //tile.y = tiles[0].y;

        switch (direction)
        {
            case Direction.up:
                tile.x = tiles[0].x;
                tile.y = tiles[0].y;
                return tile;
            case Direction.down:
                tile.x = tiles[1].x;
                tile.y = tiles[1].y;
                return tile;
            case Direction.north:
                tile.x = tiles[2].x;
                tile.y = tiles[2].y;
                return tile;
            case Direction.east:
                tile.x = tiles[3].x;
                tile.y = tiles[3].y;
                return tile;
            case Direction.south:
                tile.x = tiles[4].x;
                tile.y = tiles[4].y;
                return tile;
            case Direction.west:
                tile.x = tiles[5].x;
                tile.y = tiles[5].y;
                return tile;
        }

        

        return tile;
    }


public virtual Vector2[] FaceUVs(Direction direction)
    {
        Vector2[] UVs = new Vector2[4];
        Tile tilePos = TexturePosition(direction);

        UVs[0] = new Vector2(tileSize * tilePos.x + tileSize,
            tileSize * tilePos.y);
        UVs[1] = new Vector2(tileSize * tilePos.x + tileSize,
            tileSize * tilePos.y + tileSize);
        UVs[2] = new Vector2(tileSize * tilePos.x,
            tileSize * tilePos.y + tileSize);
        UVs[3] = new Vector2(tileSize * tilePos.x,
            tileSize * tilePos.y);
        //Debug.Log((tileSize * tilePos.x + tileSize) + ", " +
        //    (tileSize * tilePos.y));
        return UVs;
    }

    public virtual bool IsSolid(Direction direction)
    {
        switch (direction)
        {
            case Direction.north:
                return true;
            case Direction.east:
                return true;
            case Direction.south:
                return true;
            case Direction.west:
                return true;
            case Direction.up:
                return true;
            case Direction.down:
                return true;
        }

        return false;
    }

}